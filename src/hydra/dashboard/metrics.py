# ---------------------------------------------------------------------------
# Convenience update helpers
# ---------------------------------------------------------------------------


def record_trade(
    symbol: str,
    side: str,
    strategy_id: str,
    exchange_id: str,
    pnl: float | None = None,
) -> None:
    """Increment trade counter and optionally record PnL."""
    get_trades_total().labels(
        symbol=symbol,
        side=side,
        strategy_id=strategy_id,
        exchange_id=exchange_id,
    ).inc()
    if pnl is not None:
        get_trade_pnl().labels(symbol=symbol, strategy_id=strategy_id).observe(pnl)


def update_position(symbol: str, exchange_id: str, size: float) -> None:
    """Set current position size for a symbol/exchange pair."""
    get_position_size().labels(symbol=symbol, exchange_id=exchange_id).set(size)


def update_portfolio(value: float, drawdown_pct: float, daily_pnl: float) -> None:
    """Update portfolio-level gauges."""
    get_portfolio_value().set(value)
    get_drawdown_pct().set(drawdown_pct)
    get_daily_pnl().set(daily_pnl)


def record_signal(strategy_id: str, signal_type: str) -> None:
    """Increment signal counter."""
    get_signal_count().labels(strategy_id=strategy_id, signal_type=signal_type).inc()


def observe_event_bus_latency(seconds: float) -> None:
    """Record event bus message delivery latency."""
    get_event_bus_latency().observe(seconds)


def observe_exchange_api_latency(
    exchange_id: str,
    endpoint: str,
    seconds: float,
) -> None:
    """Record exchange API call latency."""
    get_exchange_api_latency().labels(
        exchange_id=exchange_id,
        endpoint=endpoint,
    ).observe(seconds)


def record_ws_reconnect(exchange_id: str) -> None:
    """Increment WebSocket reconnection counter for an exchange."""
    get_ws_reconnects().labels(exchange_id=exchange_id).inc()


def observe_order_fill_latency(seconds: float) -> None:
    """Record order fill latency."""
    get_order_fill_latency().observe(seconds)


def observe_ml_inference_latency(model_name: str, seconds: float) -> None:
    """Record ML model inference latency."""
    get_ml_inference_latency().labels(model_name=model_name).observe(seconds)


def update_data_gap(exchange_id: str, symbol: str, gap_seconds: float) -> None:
    """Set the current data gap for an exchange/symbol pair."""
    get_data_gap().labels(exchange_id=exchange_id, symbol=symbol).set(gap_seconds)


def update_reconciliation(
    exchange_id: str,
    symbol: str,
    mismatch: bool,
) -> None:
    """Set reconciliation mismatch status (1 = mismatch, 0 = ok)."""
    get_reconciliation_mismatch().labels(
        exchange_id=exchange_id,
        symbol=symbol,
    ).set(1.0 if mismatch else 0.0)


# ---------------------------------------------------------------------------
# Registry reset — used only in tests
# ---------------------------------------------------------------------------


def _reset_metrics() -> None:
    """Clear all metric collectors. For test isolation only."""
    global _initialized

    if not _initialized:
        return

    import contextlib

    from prometheus_client import REGISTRY

    for key in list(_metrics.keys()):
        with contextlib.suppress(Exception):
            REGISTRY.unregister(_metrics[key])
    _metrics.clear()
    _initialized = False
