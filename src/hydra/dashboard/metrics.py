"""Prometheus metrics definitions for the Hydra trading platform.

All metric objects are created lazily on first access so that importing
this module does **not** pull in ``prometheus_client`` at import time.
Helper functions provide a clean API for the rest of the codebase.
"""

from __future__ import annotations

from typing import TYPE_CHECKING, Any

if TYPE_CHECKING:
    from prometheus_client import Counter, Gauge, Histogram

# ---------------------------------------------------------------------------
# Lazy singleton registry
# ---------------------------------------------------------------------------

_metrics: dict[str, Any] = {}
_initialized: bool = False


def _ensure_initialized() -> None:
    """Create all Prometheus collectors on first call."""
    global _initialized
    if _initialized:
        return

    from prometheus_client import Counter, Gauge, Histogram

    # -- Trading metrics ----------------------------------------------------

    _metrics["trades_total"] = Counter(
        "hydra_trades_total",
        "Total number of executed trades",
        labelnames=["symbol", "side", "strategy_id", "exchange_id"],
    )

    _metrics["trade_pnl"] = Histogram(
        "hydra_trade_pnl",
        "PnL distribution per trade",
        labelnames=["symbol", "strategy_id"],
        buckets=(-500, -200, -100, -50, -20, -10, 0, 10, 20, 50, 100, 200, 500, 1000),
    )

    _metrics["position_size"] = Gauge(
        "hydra_position_size",
        "Current position size by symbol and exchange",
        labelnames=["symbol", "exchange_id"],
    )

    _metrics["portfolio_value"] = Gauge(
        "hydra_portfolio_value",
        "Total portfolio value in USDT",
    )

    _metrics["drawdown_pct"] = Gauge(
        "hydra_drawdown_pct",
        "Current drawdown as a percentage from peak equity",
    )

    _metrics["daily_pnl"] = Gauge(
        "hydra_daily_pnl",
        "Realized + unrealized PnL for the current day in USDT",
    )

    _metrics["signal_count"] = Counter(
        "hydra_signal_count",
        "Number of signals generated by strategy",
        labelnames=["strategy_id", "signal_type"],
    )

    # -- System metrics -----------------------------------------------------

    _metrics["event_bus_latency"] = Histogram(
        "hydra_event_bus_latency_seconds",
        "End-to-end latency of event bus message delivery",
        buckets=(0.001, 0.005, 0.01, 0.025, 0.05, 0.1, 0.25, 0.5, 1.0),
    )

    _metrics["exchange_api_latency"] = Histogram(
        "hydra_exchange_api_latency_seconds",
        "Latency of outbound exchange REST API calls",
        labelnames=["exchange_id", "endpoint"],
        buckets=(0.05, 0.1, 0.25, 0.5, 1.0, 2.5, 5.0, 10.0),
    )

    _metrics["ws_reconnects"] = Counter(
        "hydra_ws_reconnects_total",
        "Number of WebSocket reconnections per exchange",
        labelnames=["exchange_id"],
    )

    _metrics["order_fill_latency"] = Histogram(
        "hydra_order_fill_latency_seconds",
        "Time from order submission to fill confirmation",
        buckets=(0.1, 0.25, 0.5, 1.0, 2.5, 5.0, 10.0, 30.0),
    )

    _metrics["ml_inference_latency"] = Histogram(
        "hydra_ml_inference_latency_seconds",
        "Latency of ML model inference",
        labelnames=["model_name"],
        buckets=(0.001, 0.005, 0.01, 0.025, 0.05, 0.1, 0.25, 0.5),
    )

    _metrics["data_gap"] = Gauge(
        "hydra_data_gap_seconds",
        "Seconds since last received market data update",
        labelnames=["exchange_id", "symbol"],
    )

    _metrics["reconciliation_mismatch"] = Gauge(
        "hydra_reconciliation_mismatch",
        "Non-zero when local and exchange positions diverge (1 = mismatch)",
        labelnames=["exchange_id", "symbol"],
    )

    _initialized = True


# ---------------------------------------------------------------------------
# Accessor helpers — each returns the typed collector
# ---------------------------------------------------------------------------


def get_trades_total() -> Counter:
    """Return the ``hydra_trades_total`` counter."""
    _ensure_initialized()
    return _metrics["trades_total"]


